import { useState, useCallback } from "react";
import "./styles/Work.css";
import WorkImage from "./WorkImage";
import { MdArrowBack, MdArrowForward } from "react-icons/md";
import { asset } from "../assetUrl";

const projects = [
  {
    title: "Recruit-AI",
    category: "AI Hiring Assistant · B2B SaaS",
    tools: "LLM screening, Structured scoring, Auto-categorization, Candidate comms",
    image: asset("images/recruit-ai.svg"),
    link: "",
  },
  {
    title: "Profitability Calculator",
    category: "Live Web App for E-commerce Sellers",
    tools: "Multi-marketplace fees, Net profit, Margin, ROI & break-even",
    image: asset("images/profitability.svg"),
    link: "https://github.com/PrathameshBerad/OpsellAI-Seller-Profitability-Calculator",
  },
  {
    title: "SellerOS",
    category: "Seller Operations Platform · OpsellAI",
    tools: "Activation funnel, Onboarding, Connector hierarchy, Pilot readiness",
    image: asset("images/selleros.svg"),
    link: "",
  },
  {
    title: "Travonomic",
    category: "AI Travel Copilot · Consumer AI",
    tools: "Conversational planning, Intent-to-itinerary, Dynamic personalization",
    image: asset("images/travonomic.svg"),
    link: "",
  },
  {
    title: "Swiggy Multi-Cart",
    category: "Product Case Study",
    tools: "Multi-restaurant checkout, Group ordering, AOV uplift, Lower abandonment",
    image: asset("images/swiggy.svg"),
    link: "",
  },
];

const Work = () => {
  const [currentIndex, setCurrentIndex] = useState(0);
  const [isAnimating, setIsAnimating] = useState(false);

  const goToSlide = useCallback(
    (index: number) => {
      if (isAnimating) return;
      setIsAnimating(true);
      setCurrentIndex(index);
      setTimeout(() => setIsAnimating(false), 500);
    },
    [isAnimating]
  );

  const goToPrev = useCallback(() => {
    const newIndex =
      currentIndex === 0 ? projects.length - 1 : currentIndex - 1;
    goToSlide(newIndex);
  }, [currentIndex, goToSlide]);

  const goToNext = useCallback(() => {
    const newIndex =
      currentIndex === projects.length - 1 ? 0 : currentIndex + 1;
    goToSlide(newIndex);
  }, [currentIndex, goToSlide]);

  return (
    <div className="work-section" id="work">
      <div className="work-container section-container">
        <h2>
          My <span>Work</span>
        </h2>

        <div className="carousel-wrapper">
          {/* Navigation Arrows */}
          <button
            className="carousel-arrow carousel-arrow-left"
            onClick={goToPrev}
            aria-label="Previous project"
            data-cursor="disable"
          >
            <MdArrowBack />
          </button>
          <button
            className="carousel-arrow carousel-arrow-right"
            onClick={goToNext}
            aria-label="Next project"
            data-cursor="disable"
          >
            <MdArrowForward />
          </button>

          {/* Slides */}
          <div className="carousel-track-container">
            <div
              className="carousel-track"
              style={{
                transform: `translateX(-${currentIndex * 100}%)`,
              }}
            >
              {projects.map((project, index) => (
                <div className="carousel-slide" key={index}>
                  <div className="carousel-content">
                    <div className="carousel-info">
                      <div className="carousel-number">
                        <h3>0{index + 1}</h3>
                      </div>
                      <div className="carousel-details">
                        <h4>{project.title}</h4>
                        <p className="carousel-category">{project.category}</p>
                        <div className="carousel-tools">
                          <span className="tools-label">Tools &amp; Features</span>
                          <p>{project.tools}</p>
                        </div>
                      </div>
                    </div>
                    <div className="carousel-image-wrapper">
                      <WorkImage
                        image={project.image}
                        alt={project.title}
                        link={project.link}
                      />
                    </div>
                  </div>
                </div>
              ))}
            </div>
          </div>

          {/* Dot Indicators */}
          <div className="carousel-dots">
            {projects.map((_, index) => (
              <button
                key={index}
                className={`carousel-dot ${
                  index === currentIndex ? "carousel-dot-active" : ""
                }`}
                onClick={() => goToSlide(index)}
                aria-label={`Go to project ${index + 1}`}
                data-cursor="disable"
              />
            ))}
          </div>
        </div>
      </div>
    </div>
  );
};

export default Work;
